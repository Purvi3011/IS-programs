def encrypt(pt,key):
  matrix=[]
  # print(matrix)
  n=len(key)
  temp=pt
  completed=False
 
  while(len(temp)!=0):
    level=[]
    if(not completed):

      for j in range(n):
        if(len(temp)!=0):
          level.append(temp[0])
          temp=temp[1:]
        else:
          level.append('x')
          completed=True
      # print(level)
      
      
      matrix.append(level)
  
  printMat(matrix)
  # print(key)
  original_map=[]
  for i,alpha in enumerate(key):
    original_map.append([alpha,i])
  # print(original_map)

  key=list(key)
  temp=list(sorted(key))
  # print(temp)
  map=[]
  for i,alpha in enumerate(temp):
    map.append([alpha,i])

  n=len(matrix)
  # print(n)
  transpose=[]
  for i in range(len(key)):
    level=[]
    for j in range(n):
      # print(matrix[j][i])
      level.append(matrix[j][i])
    transpose.append(level)
  printMat(transpose)

  ans=""
  visited=[]
  for i in map:
    alpha=i[0]
    index=0
    for y in original_map:
      if(y[0]==alpha and y[1] not in visited):
        index=y[1]
        visited.append(index)
        break
    # print(index)
    temp=transpose[index]
    for j in temp:
      ans+=j
  # print(ans)

  return (ans,original_map,map)
  
  
