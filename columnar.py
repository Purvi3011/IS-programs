import math
def decrypt(map,o_map,key,ct):
  n_rows=math.ceil(len(ct)/len(key))
  # print(n_rows)
  text=[ct[i:i+n_rows] for i in range(0, len(ct), n_rows)]
  print(text)
  indexes=[]
  visited=[]
  for i in map:
    alpha=i[0]
    index=0
    for y in o_map:
      if(y[0]==alpha and y[1] not in visited):
        index=y[1]
        # print(index)
        visited.append(index)
        break
    indexes.append(index)
  
  # print(indexes)
  mat=[]
  for i in indexes:
    mat.append(text[i])
  # print(mat)
  
  ans=[]
  for i in range(n_rows):
    first=([word[i] for word in mat])
   
    ans.append(first)

  fans=""
  for word in ans:
    x="".join([str(i) for i in word])
    fans+=x
  # print(ans)
  # print(fans)
  index=fans.find('x')
  if index!=-1:
    fans=fans[:index]
  return fans

